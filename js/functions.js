

function askAboutMotorcycle() {
    let userHasRidden = confirm("Have you ridden a motorcycle?");
    if (userHasRidden) {
        document.write("Awesome! What's your favorite place to ride?");
    } else {
        document.write("What's stopping you from riding one?");
    }
}

